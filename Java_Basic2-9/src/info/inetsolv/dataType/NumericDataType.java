package info.inetsolv.dataType;

public class NumericDataType {

	public static void main(String[] args) {
		
		// Decimal format
		int i=123;
		System.out.println(i);
		
		// Octal Format
		int i1=0127;
		System.out.println(i1);

		
		// HexaDecimal Format
		int i2=0x127;
		System.out.println(i2);
		
		// Binary Format
		int i3=0b111;
		System.out.println(i3);
	}

}
