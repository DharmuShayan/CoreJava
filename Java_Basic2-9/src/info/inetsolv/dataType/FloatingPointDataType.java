package info.inetsolv.dataType;

public class FloatingPointDataType {

	public static void main(String[] args) {
		
		double d=10.20d;
		System.out.println(d);

		
		double d1=10e3;
		System.out.println(d1);

		
		float f=10.20f;
		System.out.println(f);

		
		float f1=10e3f;
		System.out.println(f1);
	}

}
